package org.gmnz.qet.sampling;



public class FileSamplerException extends Exception {

	private static final long serialVersionUID = 6856234938946849952L;




	public FileSamplerException(String message, Throwable cause) {
		super(message, cause);
	}




	public FileSamplerException(String message) {
		super(message);
	}




	public FileSamplerException(Throwable cause) {
		super(cause);
	}

}
